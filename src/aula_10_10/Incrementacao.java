public class Incrementacao { // e Decrementação

    public static void main(String[] args) {
        int a = 5;
        System.out.println("a: " + a);
        System.out.println("++a: " + ++a);
        System.out.println("a++: " + a++);
        System.out.println("a: " + a);
        System.out.println("--a: " + --a);
        System.out.println("a--: " + a--);
        System.out.println("a: " + a);
    }
}